package javaprograms;

public class IfelseLadder {

	public static void main(String[] args) {
		int marks =66;
		if (marks<35)
		{
			System.out.println("fail");
		}
		else if(marks<=50)
		{
			System.out.println("c grade");
		}
		else if(marks<=65) {
			System.out.println("b grade");
			
		}
		else if(marks<=75) {
			System.out.println("a grade");
		}
		else if(marks>=80) {
			System.out.println("a+ grade");
		}
		

	}

}

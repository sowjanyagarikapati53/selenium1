package practice;

public class Print1to100WithoutUsingNum 
{

	public static void main(String[] args)
	{
		int one = 'A'/'A';
		String s ="..........";
		
		for(int i=one;i<=s.length()*s.length();i++)
		{
			System.out.println(i);
		}
		
		//by using ascii
		for(int i=one;i<='d';i++) {
			System.out.println(i);
		}

	}

}
